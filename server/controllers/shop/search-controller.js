const Product = require("../../models/Product");

const searchProducts = async (req, res) => {
  try {
    // When ever we do search , we will search specifically for keyword , because we already have category filter

    const { keyword } = req.params;
    if (!keyword || typeof keyword !== "string") {
      return res.status(400).json({
        succes: false,
        message: "Keyword is required and must be in string format",
      });
    }

    // for case sensitive using regEx

    const regEx = new RegExp(keyword, "i");

    //   Based on the regEx we need to  filter the query

    const createSearchQuery = {
      $or: [
        { title: regEx },
        { description: regEx },
        { category: regEx },
        { brand: regEx },
      ],
    };

    const searchResults = await Product.find(createSearchQuery);

    res.status(200).json({
      success: true,
      data: searchResults,
    });
  } catch (error) {
    console.log(error);
    res.status(500).json({
      success: false,
      message: "Error",
    });
  }
};

module.exports = { searchProducts };
